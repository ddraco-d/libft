#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *ss;

	ss = (char *)s;
	while (*ss && *ss != (char)c)
		ss++;
	if (*ss == (char)c)
		return (ss);
	return (NULL);
}
